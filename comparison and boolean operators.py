#comparison operators

myAge = 48

myAge == 21  #this is false
myAge < 50  #this is true
myAge <= 75  #this is true
myAge >= 45  #this is true
myAge != 25  #this is false

#boolean operators

#and, or, not (use these to combine 2 or more comparisons

myAge = 48
myPet = 'dog'

myAge > 20 and myPet == 'dog'  #this is true
myAge > 20 and myPet == 'cat'  #this is false
myAge > 20 or myPet == 'cat'   #this is true

myAge > 20 and myPet == 'cat' or myPet == 'dog'  #this is true
